#!/usr/bin/env node
/**
 * create-store — scaffold a brand-new store and register it.
 *
 *   npm run create-store "<Store Name>"
 *
 * It will: 1) generate a ready-to-use store config in /clients,
 *          2) register it with the system, and
 *          3) make it the active store.
 * No code editing required — open http://localhost:3000 after `npm run dev`.
 */
import { promises as fs } from "node:fs";
import path from "node:path";
import { pathToFileURL } from "node:url";

const ROOT = process.cwd();
const CLIENTS_DIR = path.join(ROOT, "clients");
const REGISTRY = path.join(ROOT, "config", "active-client.ts");
const ENV_FILE = path.join(ROOT, ".env.local");

/* ── helpers (pure) ───────────────────────────────────────────────────────── */

export function slugify(name) {
  return String(name)
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 40);
}

/** camelCase variable name from a slug, e.g. "acme-store" → "acmeStore". */
export function toVarName(slug) {
  const camel = slug.replace(/-([a-z0-9])/g, (_, c) => c.toUpperCase());
  return /^[a-z]/.test(camel) ? camel : `store${camel.charAt(0).toUpperCase()}${camel.slice(1)}`;
}

/** Insert a line immediately before a marker comment, preserving indentation. */
export function insertBeforeMarker(source, marker, line) {
  const idx = source.indexOf(marker);
  if (idx === -1) throw new Error(`Marker not found: ${marker}`);
  const lineStart = source.lastIndexOf("\n", idx) + 1;
  const indent = source.slice(lineStart, idx);
  return source.slice(0, lineStart) + `${indent}${line}\n` + source.slice(lineStart);
}

/** Read .env.local into a key→value map (missing file → empty). */
async function readEnv() {
  try {
    const raw = await fs.readFile(ENV_FILE, "utf8");
    const out = {};
    for (const ln of raw.split(/\r?\n/)) {
      const m = ln.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*)\s*$/);
      if (m) out[m[1]] = m[2];
    }
    return out;
  } catch {
    return {};
  }
}

async function writeEnv(map) {
  const body =
    "# Generated by the ecommerce factory CLI. Safe to edit.\n" +
    Object.entries(map)
      .map(([k, v]) => `${k}=${v}`)
      .join("\n") +
    "\n";
  await fs.writeFile(ENV_FILE, body, "utf8");
}

/** Make `key` the active store, in the given mode ("production" | "demo"). */
export async function setActiveStore(key, mode = "production") {
  const env = await readEnv();
  env.PRODUCT_MODE = mode;
  env.ACTIVE_CLIENT = key;
  await writeEnv(env);
}

