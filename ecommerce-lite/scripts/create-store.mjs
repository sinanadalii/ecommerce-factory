/* ── core ─────────────────────────────────────────────────────────────────── */

export async function createStore(name, { activate = true } = {}) {
  if (!name || !name.trim()) throw new Error('Please provide a store name, e.g. npm run create-store "Acme Store"');

  let slug = slugify(name);
  if (!slug) throw new Error(`Could not derive a store id from "${name}".`);
  // Created stores are PRODUCTION by convention — never the reserved demo prefix.
  if (slug.startsWith("demo-")) slug = `store-${slug}`;

  let registry = await fs.readFile(REGISTRY, "utf8");
  if (registry.includes(`"${slug}":`)) {
    throw new Error(`A store with id "${slug}" already exists. Choose a different name.`);
  }

  const varName = toVarName(slug);

  // 1) write the store config
  await fs.mkdir(CLIENTS_DIR, { recursive: true });
  const file = path.join(CLIENTS_DIR, `${slug}.ts`);
  await fs.writeFile(file, blankStoreSource(name.trim(), slug, varName), "utf8");

  // 2) register it (import + registry entry, via the codegen markers)
  registry = insertBeforeMarker(registry, "// @factory:imports", `import { ${varName} } from "@/clients/${slug}";`);
  registry = insertBeforeMarker(registry, "// @factory:stores", `"${slug}": ${varName},`);
  await fs.writeFile(REGISTRY, registry, "utf8");

  // 3) make it the active store (production mode)
  if (activate) await setActiveStore(slug, "production");

  return { slug, varName, file };
}

/* ── CLI entry ────────────────────────────────────────────────────────────── */

